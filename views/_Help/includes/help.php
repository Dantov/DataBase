		<h3><a href="/main/?row_pos=4">Конечный центр нахождения</a></h3>
		<p>Заполняется моделями по условиям сортировки. Одна строка - одна модель.</p>
		<p>Выводит участок, на котором модель нахотится в данный момент, её статус, и дату постановки этого статуса.</p>
		<p>Корректировка дат:</p>
		<p>Таблица Рабочих центров ставит "просрочено" красным, когда модель не сдана в течении 2х суток. Опираясь на дату постановки статуса. Т.к. не все мдели можно сделать в этот промежуток - дату принятия можно изменить. </p>
		<p>Если модель находится на вашем участке и стоит статус принятия вашего участка, на месте даты появляется окно ввода. В нем можно выбрать предпологаемую дату сдачи модели. Статус "просрочено" изчезнет из строки модели в Таблице Рабочих центров.</p>
